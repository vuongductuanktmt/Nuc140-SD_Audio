int main(void)
{
  uint8_t i,j;	
  UNLOCKREG();
  SYSCLK->PWRCON.OSC22M_EN = 1; // clock source select 
  SYSCLK->CLKSEL0.HCLK_S = 7;   // HCLK select
  LOCKREG();
	
  init_MAX7219();
	
  while(1)
  {
   for(j=0;j<38;j++)
   {
    for(i=1;i<9;i++)
    write_MAX7219(i,Font8x8[j][i-1]);
    DrvSYS_Delay(100000); // 100ms
   }  
  } 
}				
